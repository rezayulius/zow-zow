        <script>
        document.addEventListener('DOMContentLoaded', function() {
            let currentStep = 1;
            const totalSteps = 4;
            
            // Elements
            const quickBookBtns = document.querySelectorAll('.quick-book-btn');
            const appointmentBtn = document.getElementById('appointmentBtn');
            const bookingForm = document.getElementById('bookingForm');
            const closeForm = document.getElementById('closeForm');
            const prevBtn = document.getElementById('prevBtn');
            const nextBtn = document.getElementById('nextBtn');
            const submitBtn = document.getElementById('submitBtn');
            const progressBar = document.getElementById('progressBar');
            const currentStepSpan = document.getElementById('currentStep');

            // Touch interaction improvements for mobile
            function addTouchInteractions() {
                // Add touch feedback for buttons
                const buttons = document.querySelectorAll('button, .cursor-pointer');
                buttons.forEach(button => {
                    button.addEventListener('touchstart', function() {
                        this.style.transform = 'scale(0.95)';
                        this.style.transition = 'transform 0.1s ease';
                    });
                    
                    button.addEventListener('touchend', function() {
                        this.style.transform = 'scale(1)';
                    });
                    
                    button.addEventListener('touchcancel', function() {
                        this.style.transform = 'scale(1)';
                    });
                });

                // Improve radio button touch targets
                const radioLabels = document.querySelectorAll('label[for*="service"], label[for*="doctor"]');
                radioLabels.forEach(label => {
                    label.style.minHeight = '44px'; // iOS recommended touch target size
                    label.style.display = 'flex';
                    label.style.alignItems = 'center';
                });

                // Add swipe gestures for form navigation
                let touchStartX = 0;
                let touchEndX = 0;
                
                bookingForm.addEventListener('touchstart', function(e) {
                    touchStartX = e.changedTouches[0].screenX;
                });
                
                bookingForm.addEventListener('touchend', function(e) {
                    touchEndX = e.changedTouches[0].screenX;
                    handleSwipe();
                });
                
                function handleSwipe() {
                    const swipeThreshold = 50;
                    const swipeDistance = touchEndX - touchStartX;
                    
                    if (Math.abs(swipeDistance) > swipeThreshold) {
                        if (swipeDistance > 0 && currentStep > 1) {
                            // Swipe right - go to previous step
                            previousStep();
                        } else if (swipeDistance < 0 && currentStep < totalSteps) {
                            // Swipe left - go to next step
                            if (validateCurrentStep()) {
                                nextStep();
                            }
                        }
                    }
                }

                // Prevent zoom on double tap for form inputs
                const inputs = document.querySelectorAll('input, select, textarea');
                inputs.forEach(input => {
                    input.addEventListener('touchend', function(e) {
                        e.preventDefault();
                        this.focus();
                    });
                });
            }

            // Initialize touch interactions
            addTouchInteractions();
            
            // Service prices
            const servicePrices = {
                'grooming': { name: 'Grooming Premium', price: 'Rp 100.000' },
                'konsultasi': { name: 'Konsultasi Dokter', price: 'Rp 150.000' },
                'vaksinasi': { name: 'Vaksinasi Lengkap', price: 'Rp 200.000' },
                'checkup': { name: 'Health Check-up', price: 'Rp 250.000' }
            };
            
            // Doctor names
            const doctorNames = {
                'dr-sarah': 'Dr. Sarah Wijaya',
                'dr-budi': 'Dr. Budi Santoso'
            };
            
            // Show/Hide form with seamless animation
            function showBookingForm(selectedService = null) {
                // Pre-select the service if provided
                if (selectedService) {
                    const serviceRadio = document.querySelector(`input[name="service"][value="${selectedService}"]`);
                    if (serviceRadio) {
                        serviceRadio.checked = true;
                    }
                }
                
                // Show form with seamless slide-down animation
                bookingForm.style.maxHeight = '0px';
                bookingForm.style.overflow = 'hidden';
                bookingForm.style.opacity = '0';
                bookingForm.classList.remove('hidden');
                
                // Trigger animation
                setTimeout(() => {
                    bookingForm.style.transition = 'all 0.6s cubic-bezier(0.4, 0, 0.2, 1)';
                    bookingForm.style.maxHeight = '1000px';
                    bookingForm.style.opacity = '1';
                    
                    // Smooth scroll to form
                    setTimeout(() => {
                        bookingForm.scrollIntoView({ 
                            behavior: 'smooth', 
                            block: 'start',
                            inline: 'nearest'
                        });
                    }, 300);
                }, 50);
            }
            
            // Appointment button handler
            appointmentBtn.addEventListener('click', function(e) {
                e.preventDefault();
                showBookingForm();
            });
            
            // Quick book buttons handler
            quickBookBtns.forEach(btn => {
                btn.addEventListener('click', function(e) {
                    e.preventDefault();
                    const selectedService = this.getAttribute('data-service');
                    showBookingForm(selectedService);
                });
            });
            
            closeForm.addEventListener('click', function() {
                // Seamless slide-up animation
                bookingForm.style.transition = 'all 0.4s cubic-bezier(0.4, 0, 0.2, 1)';
                bookingForm.style.maxHeight = '0px';
                bookingForm.style.opacity = '0';
                
                setTimeout(() => {
                    bookingForm.classList.add('hidden');
                    bookingForm.style.maxHeight = '';
                    bookingForm.style.opacity = '';
                    bookingForm.style.transition = '';
                    resetForm();
                }, 400);
            });
            
            // Navigation
            nextBtn.addEventListener('click', function() {
                if (validateStep(currentStep)) {
                    if (currentStep < totalSteps) {
                        currentStep++;
                        updateStep();
                    }
                }
            });
            
            prevBtn.addEventListener('click', function() {
                if (currentStep > 1) {
                    currentStep--;
                    updateStep();
                }
            });
            
            submitBtn.addEventListener('click', function() {
                if (validateStep(currentStep)) {
                    // Show success message
                    showSuccessMessage();
                }
            });
            
            function updateStep() {
                // Hide all steps
                document.querySelectorAll('.step-content').forEach(step => {
                    step.classList.add('hidden');
                });
                
                // Show current step
                document.getElementById('step' + currentStep).classList.remove('hidden');
                
                // Update progress bar
                const progress = (currentStep / totalSteps) * 100;
                progressBar.style.width = progress + '%';
                currentStepSpan.textContent = currentStep;
                
                // Update buttons
                prevBtn.classList.toggle('hidden', currentStep === 1);
                nextBtn.classList.toggle('hidden', currentStep === totalSteps);
                submitBtn.classList.toggle('hidden', currentStep !== totalSteps);
                
                // Update summary on step 4
                if (currentStep === 4) {
                    updateSummary();
                }
            }
            
            function validateStep(step) {
                switch(step) {
                    case 1:
                        const service = document.querySelector('input[name="service"]:checked');
                        if (!service) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Pilih Layanan',
                                text: 'Silakan pilih layanan terlebih dahulu',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        return true;
                        
                    case 2:
                        const petName = document.getElementById('petName').value.trim();
                        const petType = document.getElementById('petType').value;
                        const petAge = document.getElementById('petAge').value.trim();
                        
                        if (!petName || !petType || !petAge) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Data Tidak Lengkap',
                                text: 'Silakan lengkapi data hewan peliharaan',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        return true;
                        
                    case 3:
                        const date = document.getElementById('appointmentDate').value;
                        const time = document.getElementById('appointmentTime').value;
                        const doctor = document.querySelector('input[name="doctor"]:checked');
                        
                        if (!date || !time || !doctor) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Jadwal Belum Dipilih',
                                text: 'Silakan pilih tanggal, waktu, dan dokter',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        
                        // Check if date is not in the past
                        const selectedDate = new Date(date);
                        const today = new Date();
                        today.setHours(0, 0, 0, 0);
                        
                        if (selectedDate < today) {
                            Swal.fire({
                                icon: 'error',
                                title: 'Tanggal Tidak Valid',
                                text: 'Tanggal tidak boleh di masa lalu',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        return true;
                        
                    case 4:
                        const customerName = document.getElementById('customerName').value.trim();
                        const customerWhatsapp = document.getElementById('customerWhatsapp').value.trim();
                        const agreeTerms = document.getElementById('agreeTerms').checked;
                        
                        if (!customerName || !customerWhatsapp) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Informasi Tidak Lengkap',
                                text: 'Silakan lengkapi informasi kontak',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        
                        if (!agreeTerms) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Syarat dan Ketentuan',
                                text: 'Silakan setujui syarat dan ketentuan',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        
                        // Basic phone validation
                        if (customerWhatsapp.length < 10) {
                            Swal.fire({
                                icon: 'error',
                                title: 'Nomor Tidak Valid',
                                text: 'Nomor WhatsApp tidak valid',
                                confirmButtonColor: '#D4A574'
                            });
                            return false;
                        }
                        return true;
                        
                    default:
                        return true;
                }
            }
            
            function updateSummary() {
                // Service
                const selectedService = document.querySelector('input[name="service"]:checked');
                if (selectedService) {
                    const serviceData = servicePrices[selectedService.value];
                    document.getElementById('summaryService').textContent = serviceData.name;
                    document.getElementById('summaryPrice').textContent = serviceData.price;
                }
                
                // Pet info
                const petName = document.getElementById('petName').value || '-';
                const petType = document.getElementById('petType').value || '-';
                document.getElementById('summaryPet').textContent = `${petName} (${petType})`;
                
                // Date & Time
                const date = document.getElementById('appointmentDate').value;
                const time = document.getElementById('appointmentTime').value;
                if (date && time) {
                    const formattedDate = new Date(date).toLocaleDateString('id-ID', {
                        weekday: 'long',
                        year: 'numeric',
                        month: 'long',
                        day: 'numeric'
                    });
                    document.getElementById('summaryDateTime').textContent = `${formattedDate}, ${time} WIB`;
                }
                
                // Doctor
                const selectedDoctor = document.querySelector('input[name="doctor"]:checked');
                if (selectedDoctor) {
                    document.getElementById('summaryDoctor').textContent = doctorNames[selectedDoctor.value];
                }
            }
            
            function showSuccessMessage() {
                const customerName = document.getElementById('customerName').value;
                const customerWhatsapp = document.getElementById('customerWhatsapp').value;
                const petName = document.getElementById('petName').value;
                const petType = document.getElementById('petType').value;
                const selectedService = document.querySelector('input[name="service"]:checked');
                const serviceData = servicePrices[selectedService.value];
                const date = document.getElementById('appointmentDate').value;
                const time = document.getElementById('appointmentTime').value;
                const selectedDoctor = document.querySelector('input[name="doctor"]:checked');
                const doctorName = doctorNames[selectedDoctor.value];
                
                // Format date for display
                const formattedDate = new Date(date).toLocaleDateString('id-ID', {
                    weekday: 'long',
                    year: 'numeric',
                    month: 'long',
                    day: 'numeric'
                });
                
                // Create WhatsApp message
                const whatsappMessage = `*BOOKING APPOINTMENT ZOW-ZOW*\n\n` +
                    `👤 *Nama:* ${customerName}\n` +
                    `🐾 *Pet:* ${petName} (${petType})\n` +
                    `🏥 *Layanan:* ${serviceData.name}\n` +
                    `💰 *Harga:* ${serviceData.price}\n` +
                    `📅 *Tanggal:* ${formattedDate}\n` +
                    `⏰ *Waktu:* ${time} WIB\n` +
                    `👨‍⚕️ *Dokter:* ${doctorName}\n\n` +
                    `Mohon konfirmasi ketersediaan jadwal. Terima kasih! 🙏`;
                
                // Encode message for WhatsApp URL
                const encodedMessage = encodeURIComponent(whatsappMessage);
                const whatsappUrl = `https://wa.me/6281234567890?text=${encodedMessage}`;
                
                // Show success alert and redirect to WhatsApp
                Swal.fire({
                    icon: 'success',
                    title: 'Booking Berhasil!',
                    html: `Terima kasih <strong>${customerName}</strong>!<br>Booking untuk <strong>${petName}</strong> dengan layanan <strong>${serviceData.name}</strong> telah berhasil dibuat.<br><br>Anda akan diarahkan ke WhatsApp untuk konfirmasi.`,
                    confirmButtonText: 'Lanjut ke WhatsApp',
                    confirmButtonColor: '#25D366',
                    showCancelButton: true,
                    cancelButtonText: 'Tutup',
                    cancelButtonColor: '#6c757d'
                }).then((result) => {
                    if (result.isConfirmed) {
                        // Open WhatsApp in new tab
                        window.open(whatsappUrl, '_blank');
                    }
                    
                    // Reset form and hide
                    resetForm();
                    bookingForm.classList.add('hidden');
                });
            }
            
            function resetForm() {
                currentStep = 1;
                updateStep();
                
                // Reset all form fields
                document.querySelectorAll('input[type="text"], input[type="tel"], textarea, select').forEach(field => {
                    field.value = '';
                });
                
                document.querySelectorAll('input[type="radio"]').forEach(radio => {
                    radio.checked = false;
                });
                
                document.getElementById('appointmentDate').value = '';
            }
            
            // Set minimum date to today
            const today = new Date().toISOString().split('T')[0];
            document.getElementById('appointmentDate').setAttribute('min', today);
        });
